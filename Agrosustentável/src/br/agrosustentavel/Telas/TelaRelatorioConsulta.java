   private void carregarTabela() {
    DefaultTableModel modelo = (DefaultTableModel) jTBRelatorio.getModel();
    modelo.setRowCount(0); // limpa antes

    for (Consulta c : ConsultaDAO.listarConsultas()) {
        String tipo = "";

        boolean temAgua = c.getConsumoAgua() != null && !c.getConsumoAgua().isEmpty();
        boolean temEnergia = c.getConsumoEnergia() != null && !c.getConsumoEnergia().isEmpty();

        if (temAgua && temEnergia) {
            tipo = "√Ågua/Energia - " + c.getConsumoAgua() + "/" + c.getConsumoEnergia();
        } else if (temAgua) {
            tipo = "√Ågua - " + c.getConsumoAgua();
        } else if (temEnergia) {
            tipo = "Energia - " + c.getConsumoEnergia();
        } else {
            tipo = "Sem dados";
        }
String produtor = ProdutorDAO.buscarProdutorPorPropriedade(c.getPropriedade());

modelo.addRow(new Object[]{
    c.getPropriedade(),
    produtor,  //<-- busca direto na tabela produtor pelo nome associado √† propriedade.
    tipo,
    c.getData()
});
    }
}
 
    
