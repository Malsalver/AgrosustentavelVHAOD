    private void jTBapagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTBapagarActionPerformed
        int linhaSelecionada = jTBRelatorio.getSelectedRow();

        if (linhaSelecionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma linha para apagar.");
            return;
        }

        int confirmacao = javax.swing.JOptionPane.showConfirmDialog(this, "Deseja realmente apagar esta consulta?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirmacao == javax.swing.JOptionPane.YES_OPTION) {
            // Remove da lista de dados
        
                   ConsultaDAO.excluirConsulta(linhaSelecionada);
                      // Atualiza a tabela
        carregarTabela();
        JOptionPane.showMessageDialog(this, "Consulta removida com sucesso!");
        }
    }//GEN-LAST:event_jTBapagarActionPerformed

    private void jTBVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTBVerificarActionPerformed
        int linhaSelecionada = jTBRelatorio.getSelectedRow();

        if (linhaSelecionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma linha para verificar.");
            return;
        }

        String propriedade = (String) jTBRelatorio.getValueAt(linhaSelecionada, 0);
        String produtor = (String) jTBRelatorio.getValueAt(linhaSelecionada, 1);
        String tipo = (String) jTBRelatorio.getValueAt(linhaSelecionada, 2);
        String data = (String) jTBRelatorio.getValueAt(linhaSelecionada, 3);

        String mensagem = "Propriedade: " + propriedade +
        "\nProdutor: " + produtor +
        "\nTipo: " + tipo +
        "\nData: " + data;

        javax.swing.JOptionPane.showMessageDialog(this, mensagem, "Detalhes da Consulta", javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jTBVerificarActionPerformed

