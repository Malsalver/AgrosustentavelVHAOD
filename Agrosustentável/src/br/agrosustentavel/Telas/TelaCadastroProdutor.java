    private void jTBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTBCadastrarActionPerformed
            String nome = jTextNome.getText().trim();
            String cpf = jTextCPF.getText().trim();
            String telefone = jTextTelefone.getText().trim();
            String endereco = jTextEndereco.getText().trim();
            String propriedade = jTextPropriedade.getText().trim();

    if (nome.isEmpty() || cpf.isEmpty() || telefone.isEmpty() || endereco.isEmpty() || propriedade.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
        return;
    }

    if (!Validacao.validarCPF(cpf)) {
        JOptionPane.showMessageDialog(this, "CPF inv√°lido! Deve conter exatamente 11 n√∫meros.");
        return;
    }

    if (!Validacao.validarTelefone(telefone)) {
        JOptionPane.showMessageDialog(this, "Telefone inv√°lido! Use 10 ou 11 n√∫meros.");
        return;
    }

    Produtor novoProdutor = new Produtor(nome, cpf, telefone, endereco, propriedade);
    ProdutorDAO.cadastrarProdutor(novoProdutor);

    JOptionPane.showMessageDialog(this, "Produtor cadastrado com sucesso!");
    limparCampos();
    }//GEN-LAST:event_jTBCadastrarActionPerformed

